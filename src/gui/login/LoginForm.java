    private void cmdLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLoginActionPerformed
        String userName = txtUser.getText().trim();
        String password = String.valueOf(txtPass.getPassword()).trim();

        if (userName.isEmpty() || password.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Vui lòng nhập Mã NV và Mật khẩu!");
            return;
        }

        NhanVienDAO dao = new NhanVienDAO();
        NhanVien nv = dao.checkLogin(userName, password);

        if (nv != null) {

            Auth.user = nv;

            Application.login();

            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Xin chào, " + nv.getHoTen());
        } else {
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "Sai tên đăng nhập hoặc mật khẩu!");
        }
    }//GEN-LAST:event_cmdLoginActionPerformed

